/* USER CODE BEGIN 4 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
  if(huart->Instance == USART1)
  {

    if (rxData >= 48 && rxData <= 57)
    {
      receivedCode = receivedCode * 10 + (rxData - 48);
      digitCount++;

      if (digitCount == 4)
      {

        if (receivedCode == 1234)
        {
        	car=1;
        	open=true;
        }else if (receivedCode == 4321){
        	car=2;
        	open=true;
        }else{
        	err=true;
        }

        receivedCode = 0;
        digitCount = 0;
      }
    }

    HAL_UART_Receive_IT(&huart1, &rxData, 1);
  }
}
